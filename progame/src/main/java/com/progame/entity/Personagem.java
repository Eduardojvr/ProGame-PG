package com.progame.entity;

public class Personagem {
	private String idPersonagem;
	private String nomePersonagem;
	private String imgPersonagem;
	public String getIdPersonagem() {
		return idPersonagem;
	}
	public void setIdPersonagem(String idPersonagem) {
		this.idPersonagem = idPersonagem;
	}
	public String getNomePersonagem() {
		return nomePersonagem;
	}
	public void setNomePersonagem(String nomePersonagem) {
		this.nomePersonagem = nomePersonagem;
	}
	public String getImgPersonagem() {
		return imgPersonagem;
	}
	public void setImgPersonagem(String imgPersonagem) {
		this.imgPersonagem = imgPersonagem;
	}
	
	

}
