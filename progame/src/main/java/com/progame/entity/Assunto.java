package com.progame.entity;

public class Assunto {
	private String idConteudo;
	private String assunto;
	private String desConteudo;
	public String getIdConteudo() {
		return idConteudo;
	}
	public void setIdConteudo(String idConteudo) {
		this.idConteudo = idConteudo;
	}
	public String getAssunto() {
		return assunto;
	}
	public void setAssunto(String assunto) {
		this.assunto = assunto;
	}
	public String getDesConteudo() {
		return desConteudo;
	}
	public void setDesConteudo(String desConteudo) {
		this.desConteudo = desConteudo;
	}
	
	

}
