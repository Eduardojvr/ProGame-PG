package com.progame.entity;

public class LinguagemProgramacao {
	private String idLinguagem;
	private String nomeLinguagem;
	private String descLinguagem;
	
	public String getIdLinguagem() {
		return idLinguagem;
	}
	
	public void setIdLinguagem(String idLinguagem) {
		this.idLinguagem = idLinguagem;
	}
	
	public String getNomeLinguagem() {
		return nomeLinguagem;
	}
	
	public void setNomeLinguagem(String nomeLinguagem) {
		this.nomeLinguagem = nomeLinguagem;
	}

	public String getDescLinguagem() {
		return descLinguagem;
	}
	
	public void setDescLinguagem(String descLinguagem) {
		this.descLinguagem = descLinguagem;
	}

	
	

}
