package com.progame.dto;

public class DesafioDTO {
	private String idDesafio;
	private String desafio;
	private String idResposta;
	private String matricula;
	private String resposta;
	private String correcaoAvaliador;
	
	public String getIdDesafio() {
		return idDesafio;
	}
	public void setIdDesafio(String idDesafio) {
		this.idDesafio = idDesafio;
	}
	public String getDesafio() {
		return desafio;
	}
	public void setDesafio(String desafio) {
		this.desafio = desafio;
	}
	public String getIdResposta() {
		return idResposta;
	}
	public void setIdResposta(String idResposta) {
		this.idResposta = idResposta;
	}
	public String getMatricula() {
		return matricula;
	}
	public void setMatricula(String matricula) {
		this.matricula = matricula;
	}
	public String getResposta() {
		return resposta;
	}
	public void setResposta(String reposta) {
		this.resposta = reposta;
	}
	public String getCorrecaoAvaliador() {
		return correcaoAvaliador;
	}
	public void setCorrecaoAvaliador(String correcaoAvaliador) {
		this.correcaoAvaliador = correcaoAvaliador;
	}

	

}
