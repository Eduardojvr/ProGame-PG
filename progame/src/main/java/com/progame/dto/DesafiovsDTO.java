package com.progame.dto;

public class DesafiovsDTO {
	private String idDesafio;
	private String matriculaDesafiante;
	private String matriculaDesafiado;
	private String tituloDesafio;
	private String desafio;
	private String respostaDesafiado;
	private String resultado;
	private String avaliacao;
	
	public String getIdDesafio() {
		return idDesafio;
	}
	public void setIdDesafio(String idDesafio) {
		this.idDesafio = idDesafio;
	}
	public String getMatriculaDesafiante() {
		return matriculaDesafiante;
	}
	public void setMatriculaDesafiante(String matriculaDesafiante) {
		this.matriculaDesafiante = matriculaDesafiante;
	}
	public String getMatriculaDesafiado() {
		return matriculaDesafiado;
	}
	public void setMatriculaDesafiado(String matriculaDesafiado) {
		this.matriculaDesafiado = matriculaDesafiado;
	}
	public String getTituloDesafio() {
		return tituloDesafio;
	}
	public void setTituloDesafio(String tituloDesafio) {
		this.tituloDesafio = tituloDesafio;
	}
	public String getDesafio() {
		return desafio;
	}
	public void setDesafio(String desafio) {
		this.desafio = desafio;
	}
	public String getRespostaDesafiado() {
		return respostaDesafiado;
	}
	public void setRespostaDesafiado(String respostaDesafiado) {
		this.respostaDesafiado = respostaDesafiado;
	}
	public String getResultado() {
		return resultado;
	}
	public void setResultado(String resultado) {
		this.resultado = resultado;
	}
	public String getAvaliacao() {
		return avaliacao;
	}
	public void setAvaliacao(String avaliacao) {
		this.avaliacao = avaliacao;
	}
	
	

}
